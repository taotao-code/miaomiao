<template>
  <div class="uc-cell">
    <div @click="toDetail">
		<h2><span v-show="index!=null">{{index + 1}}.</span>{{data.title}}</h2>
		<p>{{data.des}}</p>
		<a  class="cell__anchor" v-if="isAdd" href="javascript:;" @click.stop="$emit('add-item',$event)">+</a>
	</div>
  </div>
</template>

<script>
  export default {
    name:'uc-cell',
    props:{
		data:{//{id:,title:,des:xx}
			type:Object,
			required:true
		},
		collectionName:{//集合名
			type:String,
			default:null
		},
		index:{
			type:Number,
			default:null
		},
		isAdd:{
			type:Boolean,
			default:true
		}
	},
    components:{},
    mounted(){},
    updated(){},
    methods:{
		toDetail(){
			if(this.collectionName){
				this.$router.push(`/detail/${this.data._id}?collectionName=${this.collectionName}`)
			}
		}
	}
  }
</script>

