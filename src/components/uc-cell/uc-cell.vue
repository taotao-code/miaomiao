  .uc-cell div h2{max-height:0.9rem; font-size:0.33rem; overflow:hidden;}
  .uc-cell div p{max-height:1.8rem; margin-top:0.05rem;overflow:hidden;}
  .cell__anchor{position:absolute;right:0.3rem;top:0.4rem;}
</style>
